import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.38.4";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? '',
    );

    console.log('[CHECK-STAT-REMINDERS] Starting reminder check...');

    // Call the database function to check and send reminders
    const { error } = await supabaseClient.rpc('check_stat_update_reminders');

    if (error) {
      console.error('[CHECK-STAT-REMINDERS] Error:', error);
      throw error;
    }

    console.log('[CHECK-STAT-REMINDERS] Successfully processed stat update reminders');

    return new Response(
      JSON.stringify({ success: true, message: 'Stat update reminders processed' }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200 
      }
    );

  } catch (error: any) {
    console.error('[CHECK-STAT-REMINDERS] Error:', error);
    return new Response(
      JSON.stringify({ error: error.message }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400 
      }
    );
  }
});
